public class Pair {

    int first;
    int second;

    public Pair() {};

    public Pair(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst(){
        return this.first;

    }

    public int getSecond() {
        return this.second;
    }

}
